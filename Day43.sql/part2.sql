--  Database sends result back
--  Server sends response to user


-- File System vs Database

-- File System	                Database
-- Stores data in files	    Stores data in tables
-- Hard to search          	Easy using SQL
-- No relationships        	Supports relationships
-- Low security	            High security
-- No data rules           	Constraints & validation
-- Slow for big data	        Fast & optimized


