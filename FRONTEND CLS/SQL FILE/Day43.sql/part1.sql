--  Define Database?
--  A database is an organized collection of data stored in a structured way so it can be easily accessed, managed, and updated.
--  trick:
--  Database = Digital cupboard for data



--  Difference Between Data & Database
--  Data	                                  Database
--  Raw facts or information           	Organized collection of data
--  Example: "Ravi", 22, Chennai	        Table storing many people’s details
--  Small piece                       	Big system
--  Not structured alone             	Structured & managed
--  trick: Data is ingredient → Database is Briyani pot



--  DBMS (Database Management System) is software that helps us create, manage, and interact with databases.
--  Examples: MySQL, Oracle, PostgreSQL, SQL Server
--  It allows you to:
--      Store data
--      Retrieve data
--      Update data
--      Control access
--  trick:DBMS = Manager of the database


--  What is SQL used for?
--  SQL (Structured Query Language) is used to communicate with a database.
--  It helps to:
--      Insert data  
--      Read data
--      Update data
--      Delete data
--      Create tables
--  trick: SQL = Language to talk to database

--  Example table:
--  id	name	age
--  1	Ravi	22

--  Row → (1, Ravi, 22)
--  Column → id, name, age

--  trick: Row = Person
--  Column = Person detail